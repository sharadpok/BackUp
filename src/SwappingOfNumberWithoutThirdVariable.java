
public class SwappingOfNumberWithoutThirdVariable {

	public static void main(String[] args)
	{
	    int a=10;
	    int b=20;
	    System.out.println("Number before swapping are"+" "+a+" "+b);
	    a=a+b;
	    b=a-b;
	    a=a-b;
	    System.out.println("Number after swapping are"+" "+a+" "+b);


	}

}
